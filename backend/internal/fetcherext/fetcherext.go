// FetchFullContentWithRandomUA fetches article content using a random Safari User-Agent.
// This is an extension function that wraps the base fetcher with custom UA support.
func FetchFullContentWithRandomUA(options fetcher.FetchOptions) *fetcher.FetchResult {
	options.UserAgent = randomSafariAgent()
	return fetcher.FetchFullContent(options)
}
